#Used AND
print("num1 & num2 =", num1 & num2)

#Used OR
print("num1 | num2 = ", num1 | num2)

#No operator
print("|n-num1 =", ~num1 )

#XOR operator
print("\nnum1 ^ num2 = ", num1 ^ num2)

#Used Right Shift Operator
num1 = 10
num2 = 4

print("\nnum1 >> 1 =", num1 >> 1)

print("\nnum2 >> 1 =", num2 >> 1)

